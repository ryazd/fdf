#include "fdf.h"

int			numstr(char *av)
{
	int		i;

	i = 0;
	while (av && *av && *av == ' ')
		av++;
	while (*av)
	{
		if (*av && *av != ' ')
		{
			while (*av != ' ' && *av)
				av++;
			i++;
		}
		while (*av == ' ')
			av++;
	}
	return (i);
}
