#include "fdf.h"

void	coord(int x, int y, t_fdf *data)
{
	data->x[0] = x;
	data->x[1] = x + 1;
	data->y[0] = y;
	data->y[1] = y;
}

void	coord1(int x, int y, t_fdf *data)
{
	data->x[0] = x;
	data->x[1] = x;
	data->y[0] = y;
	data->y[1] = y + 1;
}
